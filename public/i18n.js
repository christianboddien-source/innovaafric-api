function getLang() {
  return localStorage.getItem('inno_lang') ||
    (navigator.language||'').slice(0,2).toLowerCase().replace(/[^a-z]/,'') === 'fr' ? 'fr' :
    (navigator.language||'').slice(0,2).toLowerCase() === 'en' ? 'en' : 'es';
}

function applyLang(lang) {
  if(!T[lang]) lang = 'es';
  localStorage.setItem('inno_lang', lang);
  document.documentElement.lang = lang;
  const d = T[lang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if(d[k] !== undefined) el.textContent = d[k];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = el.getAttribute('data-i18n-ph');
    if(d[k] !== undefined) el.placeholder = d[k];
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const k = el.getAttribute('data-i18n-title');
    if(d[k] !== undefined) el.title = d[k];
  });

  // Update lang selector buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

function switchLang(lang) { applyLang(lang); }

// Language selector widget injected into every page
function injectLangSwitcher() {
  const style = document.createElement('style');
  style.textContent = `.lang-switcher{display:flex;gap:4px;align-items:center;}.lang-btn{background:transparent;border:1px solid #2d3748;border-radius:6px;color:#94a3b8;font-size:0.75rem;font-weight:700;padding:4px 8px;cursor:pointer;transition:all 0.2s;letter-spacing:0.5px;}.lang-btn:hover,.lang-btn.active{background:rgba(255,255,255,0.1);color:#f1f5f9;border-color:#94a3b8;}`;
  document.head.appendChild(style);

  const wrap = document.createElement('div');
  wrap.className = 'lang-switcher';
  wrap.innerHTML = `<button class="lang-btn" data-lang="es" onclick="switchLang('es')">ES</button><button class="lang-btn" data-lang="en" onclick="switchLang('en')">EN</button><button class="lang-btn" data-lang="fr" onclick="switchLang('fr')">FR</button>`;

  // Insert before nav-right or as last child of nav
  const navRight = document.querySelector('.nav-right');
  if(navRight) {
    navRight.insertBefore(wrap, navRight.firstChild);
  } else {
    const nav = document.querySelector('nav');
    if(nav) nav.appendChild(wrap);
  }
}

document.addEventListener('DOMContentLoaded', function() {
  injectLangSwitcher();
  applyLang(getLang());
});

window.switchLang = switchLang;
window.i18nT = function(key) {
  const lang = localStorage.getItem('inno_lang') || 'es';
  return (T[lang] && T[lang][key]) || (T['es'][key]) || key;
};
